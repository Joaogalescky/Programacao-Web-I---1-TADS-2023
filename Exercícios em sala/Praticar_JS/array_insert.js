let frutas = ["maçã", "banana", "laranja"];

// Adicionando um elemento no final do vetor
frutas.push("uva");

// Adicionando um elemento no início do vetor
frutas.unshift("morango");

console.log(frutas);

//Usamos o método push para adicionar um elemento ao final do vetor e o método unshift para adicionar um elemento ao início do vetor.